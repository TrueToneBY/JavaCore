import java.util.Scanner;

public class DoWhile {
    public static void main(String[] args) {
        int value;
        Scanner scanner  = new Scanner(System.in);
        do {
            System.out.println("input 5");
            value = scanner.nextInt();
        }while (value!=5);{
            System.out.println("Yu input 5");
        }
        }
}
